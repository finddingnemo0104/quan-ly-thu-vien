public class TaiKhoanDTO {
    private int id;
    private String matKhau;

    public TaiKhoanDTO(int id, String matKhau) {
        this.id = id;
        this.matKhau = matKhau;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMatKhau() {
        return matKhau;
    }

    public void setMatKhau(String matKhau) {
        this.matKhau = matKhau;
    }
    
    
}
